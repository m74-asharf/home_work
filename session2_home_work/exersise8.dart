/** Write a Dart program that declares two numeric variables and performs addition, subtraction,
 multiplication, division, and modulo on them. Print each result. */

void main() {
  num num1 = 20;
  num num2 = 3;

  print(num1 + num2);
  print(num1 - num2);
  print(num1 * num2);
  print(num1 / num2);
  print(num1 % num2);
}
